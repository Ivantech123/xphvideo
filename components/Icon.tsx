interface IconProps extends LucideProps {
  name: IconName;
}

export const Icon: React.FC<IconProps> = ({ name, ...props }) => {
  const LucideIcon = ICONS[name] as React.ElementType | undefined;
  if (!LucideIcon) return null;
  return <LucideIcon {...props} />;
};
